import math
import numpy as np

print(f"log2(16) is {math.log2(16)}")
print(f"log2(256) is {math.log2(256)}")
print(f"log2(1024) is {math.log2(1024)}")

print("------------------------------------------------")

print(f"log2(16) is {np.log2(16)}")
print(f"log2(256) is {np.log2(256)}")
print(f"log2(1024) is {np.log2(1024)}")

print("------------------------------------------------")

print(f"2^4 is {math.pow(2, 4)}")
print(f"2^8 is {math.pow(2, 8)}")
print(f"2^10 is {math.pow(2, 10)}")

print("------------------------------------------------")

print(f"2^4 is {np.power(2, 4)}")
print(f"2^8 is {np.power(2, 8)}")
print(f"2^10 is {np.power(2, 10)}")

